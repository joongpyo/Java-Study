package java04; // 비교 연산자 (관계 연산자)

public class Inequilty_sign_operator {

	public static void main(String[] args) {
			int a=10;
			int b=20;
			int c=30;
			System.out.println(a<b);//앞에 있는 문자가 주어임
			System.out.println(a>b);
			System.out.println(a+b<=c);//a+b가 c보다 작거나 같아?
			System.out.println(a+b>=c);
	}

}
